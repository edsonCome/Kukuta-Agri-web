class TranslationManager {
  constructor() {
    this.currentLanguage = 'pt'; // Idioma padrão
    this.supportedLanguages = ['pt', 'en', 'fr', 'ar', 'zh'];
    this.init();
  }

  // Inicializar o sistema
  init() {
    this.detectLanguage();
    this.createLanguageSelector();
    this.translatePage();
  }

  // Detectar idioma do navegador
  detectLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const langCode = browserLang.substring(0, 2);
    
    if (this.supportedLanguages.includes(langCode)) {
      this.currentLanguage = langCode;
    }
    
    const savedLang = localStorage.getItem('kukuta_language');
    if (savedLang && this.supportedLanguages.includes(savedLang)) {
      this.currentLanguage = savedLang;
    }
  }

  // Criar seletor de idioma
  createLanguageSelector() {
    const languageNames = {
      pt: 'Português',
      en: 'English',
      fr: 'Français',
      ar: 'العربية',
      zh: '中文'
    };

    const selectorHTML = `
      <div class="language-selector">
        <button class="current-lang" id="current-lang">
          <i class="fas fa-globe"></i>
          <span>${languageNames[this.currentLanguage]}</span>
          <i class="fas fa-chevron-down"></i>
        </button>
        <div class="language-dropdown" id="language-dropdown">
          ${this.supportedLanguages.map(lang => `
            <button class="lang-option ${lang === this.currentLanguage ? 'active' : ''}" 
                    data-lang="${lang}">
              ${languageNames[lang]}
            </button>
          `).join('')}
        </div>
      </div>
    `;

    const navActions = document.querySelector('.nav-actions');
    if (navActions) {
      navActions.insertAdjacentHTML('beforeend', selectorHTML);
      this.attachLanguageEvents();
    }
  }

  // Anexar eventos do seletor
  attachLanguageEvents() {
    const currentLangBtn = document.getElementById('current-lang');
    const dropdown = document.getElementById('language-dropdown');
    const langOptions = document.querySelectorAll('.lang-option');

    currentLangBtn?.addEventListener('click', () => {
      dropdown.classList.toggle('active');
    });

    document.addEventListener('click', (e) => {
      if (!e.target.closest('.language-selector')) {
        dropdown.classList.remove('active');
      }
    });

    langOptions.forEach(option => {
      option.addEventListener('click', () => {
        const newLang = option.dataset.lang;
        this.changeLanguage(newLang);
        dropdown.classList.remove('active');
      });
    });
  }

  // Mudar idioma
  changeLanguage(langCode) {
    if (!this.supportedLanguages.includes(langCode)) return;
    
    this.currentLanguage = langCode;
    localStorage.setItem('kukuta_language', langCode);
    
    document.documentElement.lang = langCode;

    if (langCode === 'ar') {
      document.documentElement.dir = 'rtl';
      document.body.classList.add('rtl');
    } else {
      document.documentElement.dir = 'ltr';
      document.body.classList.remove('rtl');
    }

    this.translatePage();
    this.updateLanguageSelector();

    document.dispatchEvent(new CustomEvent('languageChanged'));
  }

  // Atualizar seletor de idioma
  updateLanguageSelector() {
    const languageNames = {
      pt: 'Português',
      en: 'English',
      fr: 'Français',
      ar: 'العربية',
      zh: '中文'
    };

    const currentLangSpan = document.querySelector('#current-lang span');
    const langOptions = document.querySelectorAll('.lang-option');

    if (currentLangSpan) {
      currentLangSpan.textContent = languageNames[this.currentLanguage];
    }

    langOptions.forEach(option => {
      option.classList.toggle('active', option.dataset.lang === this.currentLanguage);
    });
  }

  // Traduzir página
  translatePage() {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
      const key = element.dataset.translate;
      const translation = this.getTranslation(key);
      
      if (translation) {
        if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'email' || element.type === 'search')) {
          element.placeholder = translation;
        } else {
          const icon = element.querySelector('i');
          if (icon && (element.tagName === 'A' || element.tagName === 'BUTTON')) {
             element.innerHTML = `${translation} ${icon.outerHTML}`;
          } else {
            element.innerHTML = translation;
          }
        }
      }
    });
  }

  // Obter tradução
  getTranslation(key) {
    const currentTranslations = translations[this.currentLanguage];
    return currentTranslations?.[key] || translations.pt[key] || key;
  }

  // Função auxiliar para uso em outros scripts
  t(key) {
    return this.getTranslation(key);
  }
}

